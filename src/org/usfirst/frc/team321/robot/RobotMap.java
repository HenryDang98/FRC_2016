package org.usfirst.frc.team321.robot;
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	
	public static final int LEFT_FRONT_MOTOR = 3;
	public static final int LEFT_MIDDLE_MOTOR = 4;
	public static final int LEFT_BACK_MOTOR = 5;
	public static final int RIGHT_FRONT_MOTOR = 0;
	public static final int RIGHT_MIDDLE_MOTOR = 1;
	public static final int RIGHT_BACK_MOTOR = 2;
	
	public static final int ENC_L_A = 0;
	public static final int ENC_L_B = 1;
	public static final int ENC_R_A = 2;
	public static final int ENC_R_B = 3;
	
	public static final int INTAKE = 7;
	public static final int INTAKE_PIVOT = 0;
	
	public static final int COMPRESSOR = 0;
}
